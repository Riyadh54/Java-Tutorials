public class TypeCasting {
    public static void main(String[] args){
          double myDouble = 9.46d;
          int myInt = (int) myDouble; // Manual casting: double to int
          // myInt = 9;
          
          float myFloat = 6.21f;
          int number = (int) myFloat;
          // number = 6;
    }
    
}
